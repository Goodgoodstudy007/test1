class Agent extends Backend
{
    
    /**
     * Agent模型对象
     * @var \app\admin\model\Agent
     */
    protected $model = null;

    public function _initialize()
    {
        parent::_initialize();
        $this->model = new \app\admin\model\Agent;

    }

    public function import()
    {
        parent::import();
    }

    /**
     * 查看
